DashboardHeader.propTypes = {
  onOpenSidebar: PropTypes.func,
  isCollapse: PropTypes.bool,
  verticalLayout: PropTypes.bool,
};

export default function DashboardHeader({ onOpenSidebar, isCollapse = false, verticalLayout = false }) {
  const isOffset = useOffSetTop(HEADER.DASHBOARD_DESKTOP_HEIGHT) && !verticalLayout;

  const isDesktop = useResponsive('up', 'lg');

  const [openWallet, setOpenWallet] = useState(false);

  const handleCloseWallet = () => {
    setOpenWallet(false);
  }

  const { themeMode, onChangeMode } = useSettings();

  return (
    <RootStyle isCollapse={isCollapse} isOffset={isOffset} verticalLayout={verticalLayout}>
      <Toolbar
        sx={{
          minHeight: '100% !important',
          px: { lg: 5 },
        }}
      >
        {isDesktop && verticalLayout && <Logo sx={{ mr: 2.5 }} />}

        {!isDesktop && (
          <IconButtonAnimate onClick={onOpenSidebar} sx={{ mr: 1, color: 'text.primary' }}>
            <Iconify icon="eva:menu-2-fill" />
          </IconButtonAnimate>
        )}

        <Box sx={{ flexGrow: 1 }} />

        <Stack direction="row" alignItems="center" spacing={{ xs: 0.5, sm: 1.5 }}>
          <FormControlLabel control={<MaterialUISwitch checked={themeMode === 'dark'} onChange={(e) => {
            if(e.target.checked) {
              onChangeMode('dark')
            }
            else {
              onChangeMode('light')
            }
          }}  />} label="" />
          {/* <IconButton id="beamer" button="beamer">
            <CampaignRounded style={{ fontSize: '26px' }} />
          </IconButton> */}
          <IconButton onClick={() => {setOpenWallet(true)}}>
            <AccountBalanceWalletRoundedIcon style={{ fontSize: '26px' }} />
          </IconButton>
          <a href="//qwikshop.zendesk.com/hc/en-us" target={"_blank"} rel="noreferrer">
          <IconButton>
            <SupportAgentIcon style={{ fontSize: '26px' }} />
          </IconButton>
          </a>
          {/* <NotificationsPopover /> */}
          {/* <ContactsPopover /> */}
          <AccountPopover />
        </Stack>
      </Toolbar>
      {openWallet && <QwikWallet open={openWallet} handleClose={handleCloseWallet} />}
    </RootStyle>
  );
}
